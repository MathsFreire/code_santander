# Variaveis
# tipos de variaveis: str(texto, string), int(numeiros inteiros), float(numeros com casas decimais), boolean(true, false, 1 or 0)

nome = 'Matheus Vieira Freire'
idade = 27
altura = 1.72
peso = 60.500
estudando = True

# obs: Só é possivel concatenar string + variais de tipo int, float, bool convertendo para str = str(variavel)
print('Nome: '+nome)
print(f'Idade: {idade}')
print(f'Altura: {altura}')
print(f'Peso: {peso}')
print(f'Estudando? {estudando}')

# como printar o tipo da variavel na tela:
print(type (nome))

